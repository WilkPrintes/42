char	*ft_strcapitalize(char *str)

{
	int	i;

	i = 1;
	if (str[0] >= 97 && str[0] <= 122 )
		str[0] = str[0] - 32;
	while (str[i] != '\0')
	{
		if ((str[i] >= 32 && str[i] <= 47))
		{
			if (str[i + 1] >= 97 && str[i + 1] <= 122)
				str[i + 1] = str[i + 1] - 32;
		}
		if ((str[i] >= 91 && str[i] <= 96) || (str[i] >= 123))
		{
			if (str[i + 1] >= 97 && str[i + 1] <= 122)
				str[i + 1] = str[i + 1] - 32;
		}
		i++;
	}
	return (str);
}
